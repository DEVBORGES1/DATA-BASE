-- CRIAR TABLE/ENTIDADE
CREATE TABLE `PLANTS` (
  `PLANT_NAME` CHAR(30) NOT NULL,
  `SENSOR_VALUE` FLOAT DEFAULT NULL,
  `SENSOR_EVENT` TIMESTAMP NOT NULL 
		DEFAULT CURRENT_TIMESTAMP 
		ON UPDATE CURRENT_TIMESTAMP,
	PRIMARY KEY `PK_PLANTS` (`PLANT_NAME`)
);


